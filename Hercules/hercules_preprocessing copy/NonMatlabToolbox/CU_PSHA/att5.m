function PGASD = att5(M,R,Ft)
%-------------------------------------------------------------------------%
%                                                                         %
%        Strong Ground-Motion Attenuation Relationship (PGA)              %
%                                                                         %
% Reference: Kobayashi et al. (2000)                                      %
%-------------------------------------------------------------------------%

%(Step 1) %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%% Set Coefficient %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

a              = 0.578;
b              = 0.00355;
e              = 0.00661;
S              = -0.069;
SR             = -0.210;
SH             = -0.114;
SM             = 0.023;
SS             = 0.237;
c              = 0.00661;
d              = 0.00661;
h              = 10; % focal depth
% Use four site categories:
 Sk            = -0.210; % Rock
%Sk            = -0.114 Hard soil
%Sk            = 0.023 Medium soil
%Sk            = 0.237 Soft soil

%(Step 2) %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%% Calculate mean PGA and Standard Deviation %%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    
PGA            = log(10^((a*M)-(b*R)-log10(R+(c*10^(d*M)))+(e*h)+(Sk))); % in unit "ln(gal)" (1 gal=0.01 m/s2)
      
T              = 0.162;
sdd            = 0.213;
SD             = sqrt((T*T)+(sdd*sdd));

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
PGASD          = [PGA SD];
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
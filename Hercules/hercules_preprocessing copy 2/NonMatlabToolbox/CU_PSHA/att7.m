function PGASD = att7(M,R,Ft)
%-------------------------------------------------------------------------%
%                                                                         %
%        Strong Ground-Motion Attenuation Relationship (PGA)              %
%                                                                         %
% Reference: Petersen et al.(2004)or Modified Youngs et al. (1997)        %
%-------------------------------------------------------------------------%

%(Step 1) %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%% Set Coefficient %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

c1             = 0.2418;
c2             = 1.414;
c3             = 0;
c4             = -2.552;
c5             = 0.00617;
c6             = 0.3846;
c7             = 1.7818;
c8             = 0.554;

%(Step 2) %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%% Calculate mean PGA and Standard Deviation %%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    
PGA1           = c1+c2*M+c3*(10-M)^2+c4*log(R+c7*exp(c8*M));
if R>200
PGA2           = PGA1+(-0.0038*(R-200));
else
PGA2           = PGA1;
end
PGA            = log(exp(PGA2)*981); % in unit "ln(gal)" (1 gal=0.01 m/s2)

if M<=8
SD             = 1.45-0.1*M;
elseif M>8
SD             = 0.65;
end

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
PGASD          = [PGA SD];
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function PGASD = att1(M,R,Ft)
%-------------------------------------------------------------------------%
%                                                                         %
%        Strong Ground-Motion Attenuation Relationship (PGA)              %
%                                                                         %
% Reference: Esteva & Villaverde (1973)                                   %
%-------------------------------------------------------------------------%

%(Step 1) %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%% Set Coefficient %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

b1             = 5600; 
b2             = 0.8; 
b3             = 2; 
b4             = 40;

b11             = 69600; 
b22             = 0.8; 
b33             = 2; 
b44             = 70;

%(Step 2) %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%% Calculate mean PGA PSA and Standard Deviations %%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

PGA            = log(b1*exp(b2*M)*((R+b4)^(-b3))); % in unit "ln(gal)" (1 gal=0.01 m/s2)
SD             = 0.64;
PSA            = log(b11*exp(b22*M)*((R+b44)^(-b33))); % in unit "ln(gal)" (1 gal=0.01 m/s2)
SD2             = 0.75;

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
PGASD          = [PGA SD PSA SD2];
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
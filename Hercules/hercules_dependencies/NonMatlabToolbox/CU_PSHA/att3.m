function PGASD = att3(M,R,Ft)
%-------------------------------------------------------------------------%
%                                                                         %
%        Strong Ground-Motion Attenuation Relationship (PGA)              %
%                                                                         %
% Reference: Sadigh et al. (1993) & Sadigh et al. (1997)                  %
%-------------------------------------------------------------------------%

%(Step 1) %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%% Set Coefficient %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

c11        = -0.624;
c12        = 1;
c13        = 0;
c14        = -2.1;
c15        = 1.29649;
c16        = 0.25;
c17        = 0;

c21        = -1.274;
c22        = 1.1;
c23        = 0;
c24        = -2.1;
c25        = -0.48451;
c26        = 0.524;
c27        = 0;
h          = 0;

%(Step 2) %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%% Calculate mean PGA and Standard Deviation %%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

R1=sqrt(R^2+h^2);
if M<=6.5
     PGA   = log(981*exp(c11+c12*M+c13*((8.5-M)^2.5)+c14*log(R1+exp(c15+c16*M))+c17*log(R1+2))); % in unit "ln(gal)" (1 gal=0.01 m/s2)
elseif M>6.5
     PGA   = log(981*exp(c21+c22*M+c23*((8.5-M)^2.5)+c24*log(R1+exp(c25+c26*M))+c27*log(R1+2))); % in unit "ln(gal)" (1 gal=0.01 m/s2)
end

if M<7.21
     SD    = 1.39-(0.14*M);
else
     SD    = 0.38;    
end

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
PGASD          = [PGA SD];
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
